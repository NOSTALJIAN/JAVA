package study_221109.ch01;

public class dataTypeEx07 {

	public static void main(String[] args) {
		//	float, double
		
		/*
		int i = 2.5;
		System.out.println(i);	//	error
		
		int f = 2.5;
		System.out.println(f);	//	error
		
		 */
		
		double d = 2.5;
		System.out.println(d);	//	2.5
		
		float f = 2.5f;
		System.out.println(f);	//	2.5
		
		double d2 = 1.0e3;		//	10.0*(10^3)
		System.out.println(d2);	//	1000.0
	}
}