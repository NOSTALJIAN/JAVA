package mine.src.test;

public class ex02 {

	public static void main(String[] args) {

		// 변수 선언하기 -> int num = 3;
		// 상수 선언하기 -> final int num = 3;
		// 상수는 다른 데이터값으로 수정이 불가능하다.
		
		final int num = 3;
		
		int number1 = 7;
		
		System.out.println(num);
		
		System.out.println(number1);
		
		// num = 7;
		// class 파일 실행 단축키 커먼드 + f11
		
		// 변수 = 데이터를 담는 그릇
		// int -> 정수 (숫자)
		// num -> 변수 이름
		
	}

}
